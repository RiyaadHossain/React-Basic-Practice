import { useEffect, useState } from 'react';
import './App.css';

function App() {
  const fontSize = {fontSize: '40px', color: 'darkblue', textShadow: '5px 5px 10px'}
  return (
    <div className="App">
      <h1 style={fontSize}>Hello</h1>
      <article style={{fontSize: '20px'}} className='blog'>
        Hey there, this is a blog written by Riyad Hossain.
        <p style={{fontWeight: '400'}}>This is a Paragraph</p>
      </article>

      <Blog head="This is my FavouriteтЮбя╕П Quote." author="I"></Blog>
      <Blog head="This is my SpecialтЖЧя╕П Quote." author="Me"></Blog>
      <Blog head="This is my ChosenЁЯП╣ Quote." author="Mine"></Blog>

      <Mobile></Mobile>
      <ToDo></ToDo>
    </div>
  );
}

const Blog = (props) => {
  return (
    <div>
      <span><h3>{props.head}</h3> - {props.author}</span>
    </div>
  )
}

const Mobile = () => {
  const [battery, setBattery] = useState(100)
  const batteryDown = () => {
    if (battery > 0) {
      setBattery(battery-10)
    }
  }
  return (
    <div>
      <h1>{ battery}</h1>
      <button className='btn' onClick={batteryDown}>Battery Down тЮЦ</button>
    </div>
  )
}

const ToDo = () => {
  const [todo, setTodo] = useState([]) // set a defualt array in the useState Function
  useEffect(() => {
    fetch('https://jsonplaceholder.typicode.com/todos')
    .then(res => res.json())
    .then(data => setTodo(data))
  }, []) // Empty array to avoid Looping
  
  return (
    <div>
      {
        todo.map(task => <h4>{task.title} - {task.completed ? "тЬЕ" : 'тЭМ' }</h4>)
      }
    </div>
  )
}

export default App;


